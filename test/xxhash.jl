using PyCall
@pyimport xxhash

@testset "Test agreement of Julia and C implementations of xxHash32" begin
    for seed in rand(UInt32, 10)
        for l in 1:100
            for _ = 1:100
                s = randstring(l)
                @test xxhash32(s, seed) == xxhash.xxh32(s, seed)[:intdigest]()
            end
        end
    end
end
