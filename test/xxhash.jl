Test against hashes generated by xxhash Python package binding to xxHash C code.

```
# $ pip install xxhash

import xxhash, random, string

hashes = []
alphabet = list(string.ascii_letters) + list(map(lambda x: str(x), range(10)))

for seed in range(3):
    seeded_hashes = []

    for l in range(1, 25):
        s = "".join(random.choices(alphabet, k=l))
        h = xxhash.xxh32(s, seed=seed).intdigest()
        seeded_hashes.append((s, h))

    hashes.append(seeded_hashes)

print(hashes)
```
